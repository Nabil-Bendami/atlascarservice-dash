create or replace function public.get_city_traffic_charts(selected_city_id int)
returns jsonb
language sql
stable
security definer
set search_path = public
as $$
  with trend as (
    select
      to_char(date_trunc('month', te.created_at), 'Mon') as name,
      count(*)::bigint as value
    from public.traffic_events te
    where te.city_id = selected_city_id
      and te.created_at >= date_trunc('year', now())
    group by 1, date_trunc('month', te.created_at)
    order by date_trunc('month', te.created_at)
  ),
  metrics as (
    select
      count(*) filter (where te.event_type = 'search')::bigint as searches,
      count(*) filter (where te.event_type in ('car_view', 'view'))::bigint as views,
      count(*) filter (where te.event_type in ('whatsapp_click', 'phone_click'))::bigint as clicks
    from public.traffic_events te
    where te.city_id = selected_city_id
  )
  select jsonb_build_object(
    'trend',
    coalesce((select jsonb_agg(jsonb_build_object('name', trend.name, 'value', trend.value)) from trend), '[]'::jsonb),
    'channelMix',
    jsonb_build_array(
      jsonb_build_object('name', 'Searches', 'value', coalesce((select searches from metrics), 0)),
      jsonb_build_object('name', 'Views', 'value', coalesce((select views from metrics), 0)),
      jsonb_build_object('name', 'Clicks', 'value', coalesce((select clicks from metrics), 0))
    )
  );
$$;

