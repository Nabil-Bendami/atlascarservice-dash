create or replace function public.current_agency_ids()
returns uuid[]
language plpgsql
stable
security definer
set search_path = public
as $$
declare
  agency_ids uuid[] := '{}'::uuid[];
begin
  if auth.uid() is null then
    return agency_ids;
  end if;

  if to_regclass('public.agencies') is not null
     and exists (
       select 1 from information_schema.columns
       where table_schema = 'public'
         and table_name = 'agencies'
         and column_name = 'owner_user_id'
     ) then
    execute 'select coalesce(array_agg(id), ''{}''::uuid[]) from public.agencies where owner_user_id = $1'
      into agency_ids
      using auth.uid();
  end if;

  if coalesce(array_length(agency_ids, 1), 0) = 0
     and to_regclass('public.profiles') is not null
     and exists (
       select 1 from information_schema.columns
       where table_schema = 'public'
         and table_name = 'profiles'
         and column_name = 'agency_id'
     ) then
    execute '
      select case
        when agency_id is null then ''{}''::uuid[]
        else array[agency_id]
      end
      from public.profiles
      where id = $1
      limit 1
    '
      into agency_ids
      using auth.uid();
  end if;

  if coalesce(array_length(agency_ids, 1), 0) = 0
     and to_regclass('public.users') is not null
     and exists (
       select 1 from information_schema.columns
       where table_schema = 'public'
         and table_name = 'users'
         and column_name = 'agency_id'
     ) then
    execute '
      select case
        when agency_id is null then ''{}''::uuid[]
        else array[agency_id]
      end
      from public.users
      where id = $1
      limit 1
    '
      into agency_ids
      using auth.uid();
  end if;

  return coalesce(agency_ids, '{}'::uuid[]);
end;
$$;

