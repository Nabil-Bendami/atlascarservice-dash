create or replace view public.city_traffic_metrics as
with traffic_rollup as (
  select
    te.city_id,
    count(*)::bigint as visitors,
    count(*) filter (where te.event_type = 'search')::bigint as searches,
    count(*) filter (where te.event_type in ('car_view', 'view'))::bigint as car_views,
    count(*) filter (where te.event_type = 'whatsapp_click')::bigint as whatsapp_clicks,
    count(*) filter (where te.event_type = 'phone_click')::bigint as phone_clicks
  from public.traffic_events te
  group by te.city_id
)
select
  city.id as city_id,
  city.name as city_name,
  coalesce(region.name, '') as region,
  city.latitude,
  city.longitude,
  coalesce(tr.visitors, 0)::bigint as visitors,
  coalesce(tr.searches, 0)::bigint as searches,
  coalesce(tr.car_views, 0)::bigint as car_views,
  coalesce(tr.whatsapp_clicks, csv.whatsapp_clicks, 0)::bigint as whatsapp_clicks,
  coalesce(tr.phone_clicks, csv.phone_clicks, 0)::bigint as phone_clicks,
  coalesce(csv.total_reservations, 0)::bigint as reservations,
  case
    when coalesce(tr.visitors, 0) = 0 then 0::numeric
    else round((coalesce(csv.total_reservations, 0)::numeric / tr.visitors::numeric) * 100, 2)
  end as conversion_rate,
  coalesce(csv.total_agencies, 0)::bigint as agencies_count,
  coalesce(csv.total_cars, 0)::bigint as cars_count,
  coalesce(csv.total_revenue, 0)::numeric(14,2) as revenue
from public.cities city
left join public.regions region on region.id = city.region_id
left join public.city_stats_view csv on csv.city_id = city.id
left join traffic_rollup tr on tr.city_id = city.id;

create or replace function public.get_owner_dashboard_stats()
returns jsonb
language sql
stable
security definer
set search_path = public
as $$
  with totals as (
    select
      count(*) filter (where a.status = 'active')::bigint as active_agencies,
      count(*) filter (where a.status = 'suspended')::bigint as suspended_agencies,
      count(*)::bigint as total_agencies
    from public.agencies a
  ),
  cars as (
    select
      count(*)::bigint as total_cars,
      count(*) filter (where coalesce(availability, 'available') = 'available' and status = 'active')::bigint as available_cars,
      count(*) filter (where coalesce(availability, 'available') = 'rented' and status = 'active')::bigint as rented_cars
    from public.cars
  ),
  reservations as (
    select
      coalesce(sum(total_reservations), 0)::bigint as total_reservations,
      coalesce(sum(total_revenue), 0)::numeric(14,2) as estimated_revenue
    from public.car_stats_view
  ),
  cities as (
    select count(*)::bigint as total_cities from public.cities
  ),
  traffic as (
    select count(*)::bigint as total_traffic from public.traffic_events
  )
  select jsonb_build_object(
    'totalAgencies', totals.total_agencies,
    'totalCars', cars.total_cars,
    'totalCities', cities.total_cities,
    'totalReservations', reservations.total_reservations,
    'availableCars', cars.available_cars,
    'rentedCars', cars.rented_cars,
    'estimatedRevenue', reservations.estimated_revenue,
    'totalTraffic', traffic.total_traffic,
    'activeAgencies', totals.active_agencies,
    'suspendedAgencies', totals.suspended_agencies
  )
  from totals, cars, reservations, cities, traffic;
$$;

