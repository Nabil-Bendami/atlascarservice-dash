declare
  reservation_amount_expr text := '0::numeric';
begin
  if exists (
    select 1 from information_schema.columns
    where table_schema = 'public' and table_name = 'reservations' and column_name = 'total_amount'
  ) then
    reservation_amount_expr := 'coalesce(r.total_amount, 0)::numeric';
  elsif exists (
    select 1 from information_schema.columns
    where table_schema = 'public' and table_name = 'reservations' and column_name = 'total_price'
  ) then
    reservation_amount_expr := 'coalesce(r.total_price, 0)::numeric';
  elsif exists (
    select 1 from information_schema.columns
    where table_schema = 'public' and table_name = 'reservations' and column_name = 'amount'
  ) then
    reservation_amount_expr := 'coalesce(r.amount, 0)::numeric';
  elsif exists (
    select 1 from information_schema.columns
    where table_schema = 'public' and table_name = 'reservations' and column_name = 'total'
  ) then
    reservation_amount_expr := 'coalesce(r.total, 0)::numeric';
  end if;

  execute format($view$
    create or replace view public.car_stats_view as
    select
      c.id as car_id,
      c.agency_id,
      count(r.id) filter (where r.status in ('accepted', 'completed'))::bigint as total_reservations,
      coalesce(
        sum(greatest((coalesce(r.end_date::date, r.start_date::date) - r.start_date::date), 1))
          filter (where r.status in ('accepted', 'completed')),
        0
      )::bigint as total_rental_days,
      coalesce(
        sum(%1$s) filter (where r.status in ('accepted', 'completed')),
        0
      )::numeric(14,2) as total_revenue,
      max(coalesce(r.end_date::timestamptz, r.created_at)) filter (where r.status in ('accepted', 'completed')) as last_rented_at,
      coalesce(c.views_count, 0)::bigint as views_count,
      coalesce(c.whatsapp_clicks, 0)::bigint as whatsapp_clicks,
      coalesce(c.phone_clicks, 0)::bigint as phone_clicks
    from public.cars c
    left join public.reservations r on r.car_id = c.id
    group by c.id;
  $view$, reservation_amount_expr);
end;
$$;

create or replace view public.agency_stats_view as
select
  a.id as agency_id,
  count(c.id)::bigint as total_cars,
  count(c.id) filter (where coalesce(c.availability, 'available') = 'available' and coalesce(c.status, 'active') = 'active')::bigint as available_cars,
  count(c.id) filter (where coalesce(c.availability, 'available') = 'rented' and coalesce(c.status, 'active') = 'active')::bigint as rented_cars,
  coalesce(sum(cs.total_reservations), 0)::bigint as total_reservations,
  coalesce(sum(cs.total_rental_days), 0)::bigint as total_rental_days,
  coalesce(sum(cs.total_revenue), 0)::numeric(14,2) as total_revenue,
  coalesce(sum(cs.views_count), 0)::bigint as views_count,
  coalesce(sum(cs.whatsapp_clicks), 0)::bigint as whatsapp_clicks,
  coalesce(sum(cs.phone_clicks), 0)::bigint as phone_clicks
from public.agencies a
left join public.cars c on c.agency_id = a.id
left join public.car_stats_view cs on cs.car_id = c.id
group by a.id;

create or replace view public.city_stats_view as
select
  city.id as city_id,
  count(distinct a.id)::bigint as total_agencies,
  count(distinct c.id)::bigint as total_cars,
  count(distinct c.id) filter (where coalesce(c.availability, 'available') = 'available' and coalesce(c.status, 'active') = 'active')::bigint as available_cars,
  coalesce(sum(cs.total_reservations), 0)::bigint as total_reservations,
  coalesce(sum(cs.total_revenue), 0)::numeric(14,2) as total_revenue,
  count(te.id)::bigint as total_traffic,
  coalesce(sum(cs.whatsapp_clicks), 0)::bigint as whatsapp_clicks,
  coalesce(sum(cs.phone_clicks), 0)::bigint as phone_clicks
from public.cities city
left join public.agencies a on a.city_id = city.id
left join public.cars c on c.agency_id = a.id
left join public.car_stats_view cs on cs.car_id = c.id
left join public.traffic_events te on te.city_id = city.id
group by city.id;

create or replace view public.owner_agencies_view as
with traffic_by_agency as (
  select
    agency_id,
    count(*)::bigint as total_traffic
  from public.traffic_events
  where agency_id is not null
  group by agency_id
)
select
  a.id,
  a.owner_user_id,
  a.email,
  coalesce(a.name, '') as name,
  a.logo_url as logo,
  a.cover_url as cover_image,
  a.city_id,
  c.name as city_name,
  coalesce(r.name, a.region, '') as region,
  a.address,
  a.phone,
  a.whatsapp,
  a.description,
  a.latitude,
  a.longitude,
  a.status,
  a.is_verified as verified,
  coalesce(ast.total_cars, 0)::bigint as cars_count,
  coalesce(ast.total_reservations, 0)::bigint as reservations_count,
  coalesce(ast.total_revenue, 0)::numeric(14,2) as estimated_revenue,
  coalesce(ast.views_count, 0)::bigint as views,
  case
    when coalesce(t.total_traffic, 0) = 0 then 0::numeric
    else round((coalesce(ast.total_reservations, 0)::numeric / t.total_traffic::numeric) * 100, 2)
  end as conversion_rate
from public.agencies a
left join public.cities c on c.id = a.city_id
left join public.regions r on r.id = c.region_id
left join public.agency_stats_view ast on ast.agency_id = a.id
left join traffic_by_agency t on t.agency_id = a.id;

